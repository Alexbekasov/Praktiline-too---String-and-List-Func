while True:
    print("\nМеню:")
    print("1 - Конкатенация строк")
    print("2 - Повторение строки")
    print("3 - Длина строки")
    print("4 - Поиск подстроки")
    print("5 - Замена в строке")
    print("6 - Разделение строки")
    print("7 - Проверка на число")
    print("8 - Перевод в верхний регистр")
    print("9 - Сборка строки из списка")
    print("10 - Перевод первой буквы в заглавную")
    print("0 - Выход")

    choice = input("Выберите действие: ")

    if choice == "1":
        s1 = input("Введите первую строку: ")
        s2 = input("Введите вторую строку: ")
        print("Результат:", s1 + " " + s2)  # Склеиваем строки

    elif choice == "2":
        s = input("Введите строку: ")
        n = int(input("Сколько раз повторить? "))
        print("Результат:", s * n)  # Повторяем строку

    elif choice == "3":
        s = input("Введите строку: ")
        print("Длина строки:", len(s))  # Показываем длину строки

    elif choice == "4":
        s = input("Введите строку: ")
        sub = input("Введите подстроку для поиска: ")
        print("Индекс первого вхождения:", s.find(sub))  # Ищем подстроку

    elif choice == "5":
        s = input("Введите строку: ")
        old = input("Что заменить: ")
        new = input("На что заменить: ")
        print("Результат:", s.replace(old, new))  # Заменяем текст

    elif choice == "6":
        s = input("Введите строку: ")
        delimiter = input("Введите разделитель: ")
        print("Список:", s.split(delimiter))  # Разделяем строку на список

    elif choice == "7":
        s = input("Введите строку: ")
        print("Это число?", s.isdigit())  # Проверяем, это число?

    elif choice == "8":
        s = input("Введите строку: ")
        print("Верхний регистр:", s.upper())  # Переводим в верхний регистр

    elif choice == "9":
        words = input("Введите слова через пробел: ").split()  # Разбиваем строку на слова
        delimiter = input("Введите разделитель: ")
        print("Результат:", delimiter.join(words))  # Соединяем слова

    elif choice == "10":
        s = input("Введите строку: ")
        print("Результат:", s.title())  # Первая буква каждого слова заглавная

    elif choice == "0":
        print("Выход из программы.")
        break  # Выход из цикла

    else:
        print("Некорректный ввод, попробуйте снова.")  # Ошибка ввода
